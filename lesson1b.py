# Python Data types
# String- It is a series of characters that are inside of quotes
name = "Ryan Mutuku"
print(name)
print(type(name))

# Numeric
# Int- indicates a whole number in python. For int we dont use quotes.
age = 18
print("The age of a person is:" ,age)
print(type (age))

# float- This is a num with a decimal

coordinates = 15.456
print("The coordinates are:", coordinates)
print(type(coordinates))


